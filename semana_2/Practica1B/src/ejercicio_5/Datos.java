package ejercicio_5;

public class Datos {
	private int max;
	private int min;
	private double prom;
	
	public Datos() {};
	
	public void setMax(int max) {
		this.max = max;
	}
	
	public int getMax() {
		return this.max;
	}
	
	public void setMin(int min) {
		this.min = min;
	}
	
	public int getMin() {
		return this.min;
	}
	
	public void setProm(double prom) {
		this.prom = prom;
	}
	
	public double getProm() {
		return this.prom;
	}
}
